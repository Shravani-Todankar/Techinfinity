// Helper function to get job by ID
export const getJobById = (id) => {
  return jobsData.find(job => job.id === parseInt(id));
};

// Helper function to get all jobs for listings
export const getAllJobs = () => {
  return jobsData.map(job => ({
    id: job.id,
    title: job.title,
    location: job.location,
    company: job.company,
    type: job.type,
    department: job.department,
    posted: job.posted,
    isHighlighted: job.isHighlighted
  }));
};